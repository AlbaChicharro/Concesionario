package util;

public class Moto {

	private String nombreMoto;
	private String matricula;
	private String marca;
	private String modelo;
	private int anio;
	private int caballos;
	
	
	public Moto(String nombreMoto, String matricula, String marca, String modelo, int anio, int caballos) {
		this.nombreMoto = nombreMoto;
		this.matricula = matricula;
		this.marca = marca;
		this.modelo = modelo;
		this.anio = anio;
		this.caballos = caballos;
	}
	
}