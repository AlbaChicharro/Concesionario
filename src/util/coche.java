package util;

public class coche {
	private String nombrecoche;
	private String matricula;
	private String marca;
	private String modelo;
	private int anio;
	
	
	public coche(private String nombrecoche, String matricula, private String marca, private String modelo, int anio) {
		this.nombrecoche = nombrecoche;
		this.matricula = matricula;
		this.marca = marca;
		this.modelo = modelo;
		this.anio = anio;
   
	}
}
